/**
 * Lua Executor Generator
 *
 * Generates the content of ivr_executor.lua — the single generic script
 * deployed to FreeSWITCH that handles ALL IVR flows.
 *
 * The script reads the flow graph at call-time from the ENRS backend API,
 * so no per-flow Lua files are needed. Only this one file is deployed.
 *
 * HTTP transport: curl via io.popen (NOT luasocket). Every other Lua script
 * in this codebase (Lua-scripts/*.lua) already uses this pattern — it avoids
 * a hard dependency on lua-socket, which is not installed by default on a
 * fresh Debian FreeSWITCH box and previously caused a fully silent call
 * failure ("module 'socket.http' not found", call just hangs up) with no
 * indication of why. cjson is used when available (usually bundled with
 * mod_lua) with a minimal pure-Lua fallback decoder/encoder so the script
 * still runs without it.
 */

export function generateIvrExecutorLua({ apiBase, apiKey, ttsEngine = 'flite|kal' }) {
  // Sanitise inputs — these are embedded verbatim in the Lua file
  const safeApiBase  = String(apiBase  || 'http://127.0.0.1:4100').replace(/"/g, '\\"');
  const safeApiKey   = String(apiKey   || '').replace(/"/g, '\\"');
  const safeTts      = String(ttsEngine || 'flite|kal').replace(/"/g, '\\"');

  return [
    '-- ============================================================',
    '-- ivr_executor.lua  —  ENRS IVR Flow Executor',
    '-- Generated by ENRS IVR Builder. DO NOT EDIT MANUALLY.',
    '-- Regenerate via: IVR Builder → Deploy button.',
    '-- ============================================================',
    '',
    '-- ── Configuration ──────────────────────────────────────────────',
    `local API_BASE     = "${safeApiBase}/api/v1/internal"`,
    `local API_KEY      = "${safeApiKey}"`,
    `local TTS_ENGINE   = "${safeTts}"   -- engine|voice format`,
    'local MAX_STEPS    = 100            -- safety cap for loop detection',
    'local HTTP_TIMEOUT = 8              -- curl timeout in seconds',
    '',
    '-- ── JSON (cjson is usually bundled with mod_lua; fall back if not) ──',
    '',
    'local json_ok, json = pcall(require, "cjson")',
    'if not json_ok then',
    '  freeswitch.consoleLog("WARN", "[ivr_executor] cjson not available, using minimal JSON fallback\\n")',
    '  json = {',
    '    decode = function(s)',
    '      local fn = load("return " .. s:gsub(\'(%b{})%s*:\', function(k) return k .. "=" end):gsub(\'"([^"]+)"\', "\'%1\'"))',
    '      local ok, v = pcall(fn or function() end)',
    '      return ok and v or nil',
    '    end,',
    '    encode = function(t)',
    '      local parts = {}',
    '      for k, v in pairs(t) do',
    '        local val',
    '        if type(v) == "string" then val = \'"\' .. v:gsub(\'"\', \'\\\\"\') .. \'"\'',
    '        elseif type(v) == "number" then val = tostring(v)',
    '        elseif type(v) == "boolean" then val = tostring(v)',
    '        end',
    '        if val then parts[#parts+1] = \'"\' .. k .. \'":\' .. val end',
    '      end',
    '      return "{" .. table.concat(parts, ",") .. "}"',
    '    end,',
    '  }',
    'end',
    '',
    '-- ── HTTP via curl (io.popen) — no luasocket dependency ────────────',
    '',
    'local function url_encode(s)',
    '  return (tostring(s or "")):gsub("[^%w%-%.%_%~]", function(c)',
    '    return string.format("%%%02X", string.byte(c))',
    '  end)',
    'end',
    '',
    'local function http_request(method, url_path, body_obj)',
    '  local url  = API_BASE .. url_path',
    '  local cmd',
    '  if method == "GET" then',
    '    cmd = string.format(',
    '      \'curl -s -m %d -H "X-Internal-Key: %s" "%s" 2>/dev/null\',',
    '      HTTP_TIMEOUT, API_KEY, url)',
    '  else',
    '    local body_str = body_obj and json.encode(body_obj) or "{}"',
    '    local safe_body = body_str:gsub("\'", "\'\\\\\'\'")',
    '    cmd = string.format(',
    '      "curl -s -m %d -X %s -H \'Content-Type: application/json\' -H \'X-Internal-Key: %s\' -d \'%s\' \'%s\' 2>/dev/null",',
    '      HTTP_TIMEOUT, method, API_KEY, safe_body, url)',
    '  end',
    '',
    '  local h    = io.popen(cmd)',
    '  local body = (h and h:read("*a")) or ""',
    '  if h then h:close() end',
    '',
    '  if body == "" then',
    '    freeswitch.consoleLog("WARN",',
    '      "[ivr_executor] " .. method .. " " .. url_path .. " => empty response\\n")',
    '    return nil',
    '  end',
    '  local ok, data = pcall(json.decode, body)',
    '  if not ok or data == nil then',
    '    freeswitch.consoleLog("ERR", "[ivr_executor] JSON parse error for " .. url_path .. ": " .. tostring(data) .. "\\n")',
    '    return nil',
    '  end',
    '  return data',
    'end',
    '',
    'local function get(p)     return http_request("GET",  p, nil)  end',
    'local function post(p, b) return http_request("POST", p, b)    end',
    '',
    '-- ── Variable interpolation ───────────────────────────────────────',
    '',
    'local function interp(s, str)',
    '  if not str then return "" end',
    '  return (str:gsub("${([^}]+)}", function(k)',
    '    return s:getVariable(k) or ""',
    '  end))',
    'end',
    '',
    '-- ── Audio resolution ─────────────────────────────────────────────',
    '',
    '-- Get sounds_dir from running FreeSWITCH at startup',
    'local _api       = freeswitch.API()',
    'local SOUNDS_DIR = _api:execute("global_getvar", "sounds_dir") or "/usr/share/freeswitch/sounds"',
    '',
    'local function resolve_audio(uri)',
    '  if not uri or uri == "" then return nil end',
    '  if uri:sub(1,7) == "/media/" then',
    '    return SOUNDS_DIR .. "/enrs/" .. uri:sub(8)',
    '  end',
    '  return uri',
    'end',
    '',
    '-- ── TTS speak ────────────────────────────────────────────────────',
    '',
    'local function speak(s, text)',
    '  if not text or text == "" then return end',
    '  s:execute("speak", TTS_ENGINE .. "|" .. text)',
    'end',
    '',
    '-- ── Node executors ───────────────────────────────────────────────',
    '',
    'local function exec_play(s, node)',
    '  local f = resolve_audio(node.audio_url)',
    '  if f then s:streamFile(f) end',
    '  return node.next',
    'end',
    '',
    'local function exec_say(s, node)',
    '  speak(s, interp(s, node.text))',
    '  return node.next',
    'end',
    '',
    'local function exec_gather(s, node)',
    '  local br      = node.branches or {}',
    '  local max_d   = node.max_digits      or 1',
    '  local timeout = (node.timeout_seconds or 10) * 1000',
    '  local terms   = node.terminators     or "#"',
    '  local digits',
    '',
    '  local pf = resolve_audio(node.prompt_audio_url)',
    '  if pf then',
    '    digits = s:playAndGetDigits(1, max_d, 3, timeout, terms, pf, "", "[0-9#*]+", "", 0)',
    '  else',
    '    local pt = interp(s, node.prompt_text)',
    '    if pt ~= "" then speak(s, pt) end',
    '    digits = s:getDigits(max_d, terms, timeout)',
    '  end',
    '',
    '  s:setVariable(node.variable_name or "gather_result", digits or "")',
    '  return br[digits] or br["_default"] or br["invalid"]',
    'end',
    '',
    'local function exec_condition(s, node)',
    '  local op  = node.operator or "=="',
    '  local val = s:getVariable(node.variable or "") or ""',
    '  local exp = interp(s, node.expected_value) or ""',
    '  local ok  = false',
    '',
    '  if     op == "==" then',
    '    ok = (val == exp)',
    '  elseif op == "!=" then',
    '    ok = (val ~= exp)',
    '  elseif op == "contains" then',
    '    ok = (val:find(exp, 1, true) ~= nil)',
    '  elseif op == "starts_with" then',
    '    ok = (val:sub(1, #exp) == exp)',
    '  elseif op == "ens_pin_valid" then',
    '    -- PIN check goes through /ens/verify-pin ONLY — it is the single',
    '    -- source of truth for pin_required + correctness (handles "no PIN',
    '    -- configured -> always authorized" internally). /ens/lookup never',
    '    -- reads a pin query param at all; the raw PIN is never echoed back',
    '    -- by lookup, matching the documented contract in CLAUDE.md.',
    '    local dest = exp ~= "" and exp or s:getVariable("destination_number") or ""',
    '    local verify = post("/ens/verify-pin", { trigger_number = dest, pin = val })',
    '    if verify and verify.authorized then',
    '      local lookup = get("/ens/lookup?number=" .. url_encode(dest))',
    '      if lookup and lookup.success and lookup.data then',
    '        s:setVariable("ens_configuration_id", tostring(lookup.data.configuration_id))',
    '        s:setVariable("ens_blast_clid",       lookup.data.blast_clid or "")',
    '      end',
    '      ok = true',
    '    end',
    '  elseif op == "ens_callback_valid" then',
    '    -- expected_value carries the reply_clid the caller dialed in on —',
    '    -- matches GET /ens/callbacks/authorize?reply_clid=&caller= exactly.',
    '    local reply_clid = exp ~= "" and exp or ""',
    '    local caller = s:getVariable("caller_id_number") or ""',
    '    local d = get("/ens/callbacks/authorize?reply_clid=" .. url_encode(reply_clid) .. "&caller=" .. url_encode(caller))',
    '    if d and d.authorized then',
    '      s:setVariable("ens_notification_uuid", d.notification_uuid or "")',
    '      s:setVariable("ens_recording_file",    d.recording_file or "")',
    '      s:setVariable("ens_delivery_id",       tostring(d.delivery_id or ""))',
    '      ok = true',
    '    end',
    '  end',
    '',
    '  return ok and node.true_node or node.false_node',
    'end',
    '',
    'local function exec_goto(s, node)  return node.target_node_id end',
    '',
    'local function exec_ens(s, node)',
    '  local cfg_id = node.ens_configuration_id',
    '  if not cfg_id or cfg_id == 0 then',
    '    cfg_id = tonumber(s:getVariable(node.ens_config_var or "ens_configuration_id") or "")',
    '  end',
    '  if not cfg_id then',
    '    freeswitch.consoleLog("ERR", "[ivr_executor] ens node: could not resolve configuration_id — skipping\\n")',
    '    return node.next',
    '  end',
    '',
    '  local caller_number = s:getVariable("caller_id_number") or ""',
    '  local recording_file',
    '  if node.recording_file_var and node.recording_file_var ~= "" then',
    '    local rf = s:getVariable(node.recording_file_var) or ""',
    '    if rf ~= "" then recording_file = rf end',
    '  end',
    '',
    '  local d = post("/ens/notifications", {',
    '    configuration_id = cfg_id,',
    '    triggered_via    = "PHONE",',
    '    caller_number    = caller_number ~= "" and caller_number or nil,',
    '    recording_file   = recording_file,',
    '  })',
    '',
    '  if d and d.notification_uuid then',
    '    s:setVariable("ens_notification_uuid", d.notification_uuid)',
    '    freeswitch.consoleLog("INFO", "[ivr_executor] ens node: blast triggered uuid=" .. d.notification_uuid .. "\\n")',
    '  else',
    '    freeswitch.consoleLog("ERR", "[ivr_executor] ens node: blast failed — " .. tostring(d and d.error or "no response") .. "\\n")',
    '  end',
    '',
    '  return node.next',
    'end',
    '',
    'local function exec_ers(s, node)',
    '  local cfg_id = node.ers_configuration_id',
    '  if not cfg_id or cfg_id == 0 then',
    '    cfg_id = tonumber(s:getVariable(node.ers_config_var or "ers_configuration_id") or "")',
    '  end',
    '  if not cfg_id then',
    '    freeswitch.consoleLog("ERR", "[ivr_executor] ers node: could not resolve configuration_id — hanging up\\n")',
    '    return nil',
    '  end',
    '',
    '  -- The API never echoes conference_room back — it is generated here and',
    '  -- reused directly to bridge, matching the constraint the internal API',
    '  -- enforces on the value it is given: ^[a-z0-9_]{1,64}$',
    '  local room = "ers_" .. tostring(cfg_id) .. "_" .. tostring(os.time())',
    '',
    '  local d = post("/ers/incidents", {',
    '    configuration_id = cfg_id,',
    '    caller_number    = s:getVariable("caller_id_number") or "",',
    '    conference_room  = room,',
    '    group_type       = node.group_type or "primary",',
    '  })',
    '',
    '  if d and d.incident_uuid then',
    '    s:setVariable("ers_incident_uuid", d.incident_uuid)',
    '    s:execute("conference", room .. "@default")',
    '    -- s:execute("conference", ...) blocks until THIS leg leaves the',
    '    -- room. Reuse the already-built /ers/incidents/:uuid/complete',
    '    -- endpoint (handles ers_queues promotion too — do not rebuild that',
    '    -- here) so the incident row stops showing as permanently ACTIVE.',
    '    -- NOTE: this marks "this leg left," not "the room is empty" — a',
    '    -- room with other members still bridged stays live in FreeSWITCH',
    '    -- regardless of this incident row\'s status; live occupancy must',
    '    -- always be checked via ESL conference member count, never this',
    '    -- status field alone.',
    '    post("/ers/incidents/" .. d.incident_uuid .. "/complete", {',
    '      recording_file = s:getVariable("recorded_file_path") or nil,',
    '    })',
    '  else',
    '    freeswitch.consoleLog("ERR", "[ivr_executor] ers node: incident creation failed — " .. tostring(d and d.error or "no response") .. "\\n")',
    '  end',
    '  return nil',
    'end',
    '',
    'local function exec_hangup(s, node)',
    '  local f = resolve_audio(node.play_audio_url)',
    '  if f then s:streamFile(f) end',
    '  s:hangup()',
    '  return nil',
    'end',
    '',
    'local function exec_record_message(s, node)',
    '  local rec_dir = node.record_dir',
    '  if not rec_dir or rec_dir == "" then',
    '    rec_dir = _api:execute("global_getvar", "recordings_dir") or "/var/lib/freeswitch/recordings"',
    '    rec_dir = rec_dir .. "/ivr"',
    '  end',
    '  local fpath = rec_dir .. "/ivr_" .. s:getVariable("uuid") .. "_" .. os.time() .. ".wav"',
    '',
    '  local pf = resolve_audio(node.prompt_audio_url)',
    '  if pf then',
    '    s:streamFile(pf)',
    '  else',
    '    local pt = interp(s, node.prompt_text) or ""',
    '    if pt ~= "" then speak(s, pt) end',
    '  end',
    '',
    '  s:execute("playback", "tone_stream://%(500,0,640)")  -- recording beep',
    '  s:recordFile(fpath,',
    '    node.max_seconds       or 60,',
    '    node.silence_threshold or 500,',
    '    node.silence_hits      or 3)',
    '  s:setVariable(node.variable_name or "recorded_file_path", fpath)',
    '  return node.next',
    'end',
    '',
    'local function exec_set_variable(s, node)',
    '  s:setVariable(node.variable or "unknown_var", interp(s, node.value) or "")',
    '  return node.next',
    'end',
    '',
    'local function exec_transfer(s, node)',
    '  local dest = interp(s, node.destination) or ""',
    '  s:execute("transfer",',
    '    dest .. " " .. (node.dialplan or "XML") .. " " .. (node.context or "default"))',
    '  return nil',
    'end',
    '',
    '-- ── Executor dispatch table ───────────────────────────────────────',
    '-- NOTE: "goto" is a reserved word since Lua 5.2 and cannot be used as a',
    '-- bare table-constructor key (`goto = fn` is a syntax error) — it must',
    '-- be written as a string-indexed key.',
    '',
    'local EXEC = {',
    '  play           = exec_play,',
    '  say            = exec_say,',
    '  gather         = exec_gather,',
    '  condition      = exec_condition,',
    '  ["goto"]       = exec_goto,',
    '  ens            = exec_ens,',
    '  ers            = exec_ers,',
    '  hangup         = exec_hangup,',
    '  record_message = exec_record_message,',
    '  set_variable   = exec_set_variable,',
    '  transfer       = exec_transfer,',
    '}',
    '',
    '-- ── Main ─────────────────────────────────────────────────────────',
    '',
    'if not session:ready() then return end',
    '',
    'session:answer()',
    'session:setAutoHangup(false)',
    '',
    'local dest = session:getVariable("destination_number") or ""',
    'freeswitch.consoleLog("INFO", "[ivr_executor] Fetching IVR for number=" .. dest .. "\\n")',
    '',
    '-- entry_node_id and nodes are TOP-LEVEL fields on the lookup response,',
    '-- not nested under a "graph" key.',
    'local data = get("/ivr/lookup?number=" .. url_encode(dest))',
    'if not data or not data.entry_node_id then',
    '  freeswitch.consoleLog("ERR",',
    '    "[ivr_executor] No IVR flow found for number=" .. dest .. "\\n")',
    '  session:hangup("NO_ROUTE_DESTINATION")',
    '  return',
    'end',
    '',
    'local nodes  = data.nodes or {}',
    'local cur_id = data.entry_node_id',
    'local steps  = 0',
    '',
    'while cur_id and steps < MAX_STEPS and session:ready() do',
    '  steps = steps + 1',
    '  local node = nodes[cur_id]',
    '  if not node then',
    '    freeswitch.consoleLog("ERR",',
    '      "[ivr_executor] Node not found: " .. tostring(cur_id) .. "\\n")',
    '    break',
    '  end',
    '  local fn = EXEC[node.type]',
    '  if not fn then',
    '    freeswitch.consoleLog("WARN",',
    '      "[ivr_executor] Unknown node type: " .. tostring(node.type) .. "\\n")',
    '    break',
    '  end',
    '  freeswitch.consoleLog("DEBUG",',
    '    "[ivr_executor] step=" .. steps .. " node=" .. cur_id .. " type=" .. node.type .. "\\n")',
    '  local ok, next_id = pcall(fn, session, node)',
    '  if not ok then',
    '    freeswitch.consoleLog("ERR",',
    '      "[ivr_executor] Error in node " .. cur_id .. ": " .. tostring(next_id) .. "\\n")',
    '    break',
    '  end',
    '  cur_id = next_id',
    'end',
    '',
    'if steps >= MAX_STEPS then',
    '  freeswitch.consoleLog("WARN",',
    '    "[ivr_executor] MAX_STEPS (" .. MAX_STEPS .. ") reached for number=" .. dest .. "\\n")',
    'end',
    '',
    'if session:ready() then session:hangup() end',
  ].join('\n');
}

/**
 * Lua Executor Generator
 *
 * Generates the content of ivr_executor.lua — the single generic script
 * deployed to FreeSWITCH that handles ALL IVR flows.
 *
 * The script reads the flow graph at call-time from the ENRS backend API,
 * so no per-flow Lua files are needed. Only this one file is deployed.
 *
 * Requirements on the FreeSWITCH server:
 *   apt-get install lua-socket lua-cjson    (Debian/Ubuntu)
 *   yum install lua-socket lua-cjson        (RHEL/CentOS)
 *   mod_flite or mod_espeak loaded for TTS  (optional — only for say nodes)
 */

export function generateIvrExecutorLua({ apiBase, apiKey, ttsEngine = 'flite|kal' }) {
  // Sanitise inputs — these are embedded verbatim in the Lua file
  const safeApiBase  = String(apiBase  || 'http://127.0.0.1:4100').replace(/"/g, '\\"');
  const safeApiKey   = String(apiKey   || '').replace(/"/g, '\\"');
  const safeTts      = String(ttsEngine || 'flite|kal').replace(/"/g, '\\"');

  return [
    '-- ============================================================',
    '-- ivr_executor.lua  —  ENRS IVR Flow Executor',
    '-- Generated by ENRS IVR Builder. DO NOT EDIT MANUALLY.',
    '-- Regenerate via: IVR Builder → Deploy button.',
    '-- ============================================================',
    '-- LuaSocket + cjson must be installed on the FreeSWITCH host.',
    '-- Debian/Ubuntu:  apt-get install lua-socket lua-cjson',
    '',
    '-- ── Configuration ──────────────────────────────────────────────',
    `local API_BASE   = "${safeApiBase}"`,
    `local API_KEY    = "${safeApiKey}"`,
    `local TTS_ENGINE = "${safeTts}"   -- engine|voice format`,
    'local MAX_STEPS  = 100            -- safety cap for loop detection',
    '',
    '-- ── HTTP Helper ─────────────────────────────────────────────────',
    '',
    'local http = require("socket.http")',
    'local ltn12 = require("ltn12")',
    'local json  = require("cjson")',
    '',
    'local function http_request(method, url_path, body_obj)',
    '  local url      = API_BASE .. url_path',
    '  local body_str = body_obj and json.encode(body_obj) or nil',
    '  local chunks   = {}',
    '  local _, code  = http.request {',
    '    url    = url,',
    '    method = method,',
    '    headers = {',
    '      ["x-internal-key"] = API_KEY,',
    '      ["content-type"]   = body_str and "application/json" or nil,',
    '      ["content-length"] = body_str and tostring(#body_str) or nil,',
    '    },',
    '    source = body_str and ltn12.source.string(body_str) or nil,',
    '    sink   = ltn12.sink.table(chunks),',
    '  }',
    '  if not code or (code ~= 200 and code ~= 201) then',
    '    freeswitch.consoleLog("ERR",',
    '      "[ivr_executor] HTTP " .. method .. " " .. url_path .. " => " .. tostring(code) .. "\\n")',
    '    return nil',
    '  end',
    '  local body = table.concat(chunks)',
    '  if #body == 0 then return {} end',
    '  local ok, data = pcall(json.decode, body)',
    '  if not ok then',
    '    freeswitch.consoleLog("ERR", "[ivr_executor] JSON parse error: " .. tostring(data) .. "\\n")',
    '    return nil',
    '  end',
    '  return data',
    'end',
    '',
    'local function get(p)     return http_request("GET",  p, nil)  end',
    'local function post(p, b) return http_request("POST", p, b)    end',
    '',
    '-- ── Variable interpolation ───────────────────────────────────────',
    '',
    'local function interp(s, str)',
    '  if not str then return "" end',
    '  return (str:gsub("${([^}]+)}", function(k)',
    '    return s:getVariable(k) or ""',
    '  end))',
    'end',
    '',
    '-- ── Audio resolution ─────────────────────────────────────────────',
    '',
    '-- Get sounds_dir from running FreeSWITCH at startup',
    'local _api       = freeswitch.API()',
    'local SOUNDS_DIR = _api:execute("global_getvar", "sounds_dir") or "/usr/share/freeswitch/sounds"',
    '',
    'local function resolve_audio(uri)',
    '  if not uri or uri == "" then return nil end',
    '  if uri:sub(1,7) == "/media/" then',
    '    return SOUNDS_DIR .. "/enrs/" .. uri:sub(8)',
    '  end',
    '  return uri',
    'end',
    '',
    '-- ── TTS speak ────────────────────────────────────────────────────',
    '',
    'local function speak(s, text)',
    '  if not text or text == "" then return end',
    '  s:execute("speak", TTS_ENGINE .. "|" .. text)',
    'end',
    '',
    '-- ── Node executors ───────────────────────────────────────────────',
    '',
    'local function exec_play(s, node)',
    '  local f = resolve_audio(node.audio_url)',
    '  if f then s:streamFile(f) end',
    '  return node.next',
    'end',
    '',
    'local function exec_say(s, node)',
    '  speak(s, interp(s, node.text))',
    '  return node.next',
    'end',
    '',
    'local function exec_gather(s, node)',
    '  local br      = node.branches or {}',
    '  local max_d   = node.max_digits      or 1',
    '  local timeout = (node.timeout_seconds or 10) * 1000',
    '  local terms   = node.terminators     or "#"',
    '  local digits',
    '',
    '  local pf = resolve_audio(node.prompt_audio_url)',
    '  if pf then',
    '    digits = s:playAndGetDigits(1, max_d, 3, timeout, terms, pf, "", "[0-9#*]+", "", 0)',
    '  else',
    '    local pt = interp(s, node.prompt_text)',
    '    if pt ~= "" then speak(s, pt) end',
    '    digits = s:getDigits(max_d, terms, timeout)',
    '  end',
    '',
    '  s:setVariable(node.variable_name or "gather_result", digits or "")',
    '  return br[digits] or br["_default"] or br["invalid"]',
    'end',
    '',
    'local function exec_condition(s, node)',
    '  local op  = node.operator or "=="',
    '  local val = s:getVariable(node.variable or "") or ""',
    '  local exp = interp(s, node.expected_value) or ""',
    '  local ok  = false',
    '',
    '  if     op == "==" then',
    '    ok = (val == exp)',
    '  elseif op == "!=" then',
    '    ok = (val ~= exp)',
    '  elseif op == "contains" then',
    '    ok = (val:find(exp, 1, true) ~= nil)',
    '  elseif op == "starts_with" then',
    '    ok = (val:sub(1, #exp) == exp)',
    '  elseif op == "ens_pin_valid" then',
    '    local dest = exp ~= "" and exp or s:getVariable("destination_number") or ""',
    '    local d = get("/api/v1/internal/ens/lookup?number=" .. dest .. "&pin=" .. val)',
    '    if d and d.configuration_id then',
    '      s:setVariable("ens_configuration_id", tostring(d.configuration_id))',
    '      s:setVariable("ens_blast_clid",       d.blast_clid  or "")',
    '      s:setVariable("ens_retry_count",      tostring(d.retry_count or 0))',
    '      ok = true',
    '    end',
    '  elseif op == "ens_callback_valid" then',
    '    local caller = s:getVariable("caller_id_number") or ""',
    '    local d = get("/api/v1/internal/ens/callback_lookup?caller=" .. caller)',
    '    if d and d.delivery_id then',
    '      s:setVariable("ens_delivery_id",     tostring(d.delivery_id))',
    '      s:setVariable("ens_notification_id", tostring(d.notification_id or ""))',
    '      ok = true',
    '    end',
    '  end',
    '',
    '  return ok and node.true_node or node.false_node',
    'end',
    '',
    'local function exec_goto(s, node)  return node.target_node_id end',
    '',
    'local function exec_ens(s, node)',
    '  local cfg_id = node.ens_configuration_id',
    '  if not cfg_id or cfg_id == 0 then',
    '    cfg_id = tonumber(s:getVariable(node.ens_config_var or "ens_configuration_id") or "")',
    '  end',
    '  if cfg_id then',
    '    post("/api/v1/internal/ens/trigger", {',
    '      ens_configuration_id = cfg_id,',
    '      caller_number        = s:getVariable("caller_id_number") or "",',
    '      recording_file       = s:getVariable(node.recording_file_var or "recorded_file_path") or "",',
    '    })',
    '  end',
    '  return node.next',
    'end',
    '',
    'local function exec_ers(s, node)',
    '  local d = post("/api/v1/internal/ers/start", {',
    '    ers_configuration_id = node.ers_configuration_id or 0,',
    '    caller_number        = s:getVariable("caller_id_number") or "",',
    '    destination_number   = s:getVariable("destination_number") or "",',
    '  })',
    '  if d and d.conference_room then',
    '    s:execute("conference", d.conference_room .. "@default")',
    '  end',
    '  return nil',
    'end',
    '',
    'local function exec_hangup(s, node)',
    '  local f = resolve_audio(node.play_audio_url)',
    '  if f then s:streamFile(f) end',
    '  s:hangup()',
    '  return nil',
    'end',
    '',
    'local function exec_record_message(s, node)',
    '  local rec_dir = node.record_dir',
    '  if not rec_dir or rec_dir == "" then',
    '    rec_dir = _api:execute("global_getvar", "recordings_dir") or "/var/lib/freeswitch/recordings"',
    '    rec_dir = rec_dir .. "/ivr"',
    '  end',
    '  local fpath = rec_dir .. "/ivr_" .. s:getVariable("uuid") .. "_" .. os.time() .. ".wav"',
    '',
    '  local pf = resolve_audio(node.prompt_audio_url)',
    '  if pf then',
    '    s:streamFile(pf)',
    '  else',
    '    local pt = interp(s, node.prompt_text) or ""',
    '    if pt ~= "" then speak(s, pt) end',
    '  end',
    '',
    '  s:execute("playback", "tone_stream://%(500,0,640)")  -- recording beep',
    '  s:recordFile(fpath,',
    '    node.max_seconds       or 60,',
    '    node.silence_threshold or 500,',
    '    node.silence_hits      or 3)',
    '  s:setVariable(node.variable_name or "recorded_file_path", fpath)',
    '  return node.next',
    'end',
    '',
    'local function exec_set_variable(s, node)',
    '  s:setVariable(node.variable or "unknown_var", interp(s, node.value) or "")',
    '  return node.next',
    'end',
    '',
    'local function exec_transfer(s, node)',
    '  local dest = interp(s, node.destination) or ""',
    '  s:execute("transfer",',
    '    dest .. " " .. (node.dialplan or "XML") .. " " .. (node.context or "default"))',
    '  return nil',
    'end',
    '',
    '-- ── Executor dispatch table ───────────────────────────────────────',
    '',
    'local EXEC = {',
    '  play           = exec_play,',
    '  say            = exec_say,',
    '  gather         = exec_gather,',
    '  condition      = exec_condition,',
    '  goto           = exec_goto,',
    '  ens            = exec_ens,',
    '  ers            = exec_ers,',
    '  hangup         = exec_hangup,',
    '  record_message = exec_record_message,',
    '  set_variable   = exec_set_variable,',
    '  transfer       = exec_transfer,',
    '}',
    '',
    '-- ── Main ─────────────────────────────────────────────────────────',
    '',
    'if not session:ready() then return end',
    '',
    'session:answer()',
    'session:setAutoHangup(false)',
    '',
    'local dest = session:getVariable("destination_number") or ""',
    'freeswitch.consoleLog("INFO", "[ivr_executor] Fetching IVR for number=" .. dest .. "\\n")',
    '',
    'local data = get("/api/v1/internal/ivr/lookup?number=" .. dest)',
    'if not data or not data.graph then',
    '  freeswitch.consoleLog("ERR",',
    '    "[ivr_executor] No IVR flow found for number=" .. dest .. "\\n")',
    '  session:hangup("NO_ROUTE_DESTINATION")',
    '  return',
    'end',
    '',
    'local graph  = data.graph',
    'local nodes  = graph.nodes  or {}',
    'local cur_id = graph.entry_node_id',
    'local steps  = 0',
    '',
    'while cur_id and steps < MAX_STEPS and session:ready() do',
    '  steps = steps + 1',
    '  local node = nodes[cur_id]',
    '  if not node then',
    '    freeswitch.consoleLog("ERR",',
    '      "[ivr_executor] Node not found: " .. tostring(cur_id) .. "\\n")',
    '    break',
    '  end',
    '  local fn = EXEC[node.type]',
    '  if not fn then',
    '    freeswitch.consoleLog("WARN",',
    '      "[ivr_executor] Unknown node type: " .. tostring(node.type) .. "\\n")',
    '    break',
    '  end',
    '  freeswitch.consoleLog("DEBUG",',
    '    "[ivr_executor] step=" .. steps .. " node=" .. cur_id .. " type=" .. node.type .. "\\n")',
    '  local ok, next_id = pcall(fn, session, node)',
    '  if not ok then',
    '    freeswitch.consoleLog("ERR",',
    '      "[ivr_executor] Error in node " .. cur_id .. ": " .. tostring(next_id) .. "\\n")',
    '    break',
    '  end',
    '  cur_id = next_id',
    'end',
    '',
    'if steps >= MAX_STEPS then',
    '  freeswitch.consoleLog("WARN",',
    '    "[ivr_executor] MAX_STEPS (" .. MAX_STEPS .. ") reached for number=" .. dest .. "\\n")',
    'end',
    '',
    'if session:ready() then session:hangup() end',
  ].join('\n');
}
